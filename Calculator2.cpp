#include <iostream>

using namespace std;

int main() {
    long long n,m;
    cin >> n;
    cin >> m;
    cout << n << " + " << m << " = "<< n+m << endl;
    cout << "\n";
    cout << n << " - " << m << " = "<< n-m << endl;
    cout << "\n";
    cout << n << " * " << m << " = "<< n*m << endl;
    cout << "\n";
    cout << n << " / " << m << " = "<< n/m << endl;
    cout << "\n";
    cout << n << " % " << m << " = "<< n%m << endl;
   
}